    private void btnConfirmarCitaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarCitaActionPerformed
                                              
    try {
        // Capturar los datos ingresados por la recepcionista
        String curp = txtCurp.getText().trim(); // CURP del paciente
        String horaTexto = (String) jComboBox1.getSelectedItem(); // Hora seleccionada en formato HH:mm
        String fechaTexto = txtFecha.getText().trim(); // Fecha seleccionada en formato yyyy-MM-dd

        // Validar que los campos no est√©n vac√≠os
        if (curp.isEmpty() || horaTexto == null || fechaTexto.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, completa todos los campos.");
            return;
        }

        // Convertir la hora al formato completo (HH:mm:ss)
        String horaCompleta = horaTexto + ":00";

        // Crear una instancia del controlador
        AgendarCitasController controlador = new AgendarCitasController();

        // Llamar al m√©todo confirmarCita del controlador
        controlador.confirmarCita(curp, horaCompleta, fechaTexto, jtxtAreaCitas, jCalendarCitas);

    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Error al confirmar la cita: " + ex.getMessage());
    }
    }//GEN-LAST:event_btnConfirmarCitaActionPerformed

