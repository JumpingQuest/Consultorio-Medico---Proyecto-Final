  private void buscarPacientePorCurp(String curp) {
    // Verificar si la CURP tiene exactamente 18 caracteres
    if (curp.length() != 18) {
        limpiarCamposPaciente(); // Limpia las cajas de texto
        return; // No hace nada si no son exactamente 18 caracteres
    }

    String query = "SELECT nombre, apellido, correo, telefono FROM Pacientes WHERE curp = ?";
    try (Connection conn = ConexionDB.conectar();
         PreparedStatement pstmt = conn.prepareStatement(query)) {

        pstmt.setString(1, curp);
        ResultSet rs = pstmt.executeQuery();

        if (rs.next()) {
            // Si existe, llenar los campos
            txtNombre.setText(rs.getString("nombre"));
            txtApellido.setText(rs.getString("apellido"));
            txtCorreo.setText(rs.getString("correo"));
            txtTelefono.setText(rs.getString("telefono"));
        } else {
            // Si no existe, limpiar los campos y mostrar el mensaje
            limpiarCamposPaciente();
            JOptionPane.showMessageDialog(this, "No se encontr√≥ un paciente con esa CURP.");
        }
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Error al buscar paciente: " + ex.getMessage());
    }
}


private void limpiarCamposPaciente() {
    txtFecha.setText("");
    txtApellido.setText("");
    txtCorreo.setText("");
    txtTelefono.setText("");
}




    
